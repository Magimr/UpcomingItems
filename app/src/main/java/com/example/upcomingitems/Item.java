package com.example.upcomingitems;

public class Item {

    public String image;
    public Images images;
    public String captial;
    public String type;
    public String rarity;
    public String obtained;
    public String obtained_type;


    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Images getImages() {
        return images;
    }

    public void setImages(Images images) {
        this.images = images;
    }

    public String getCaptial() {
        return captial;
    }

    public void setCaptial(String captial) {
        this.captial = captial;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRarity() {
        return rarity;
    }

    public void setRarity(String rarity) {
        this.rarity = rarity;
    }

    public String getObtained() {
        return obtained;
    }

    public void setObtained(String obtained) {
        this.obtained = obtained;
    }

    public String getObtained_type() {
        return obtained_type;
    }

    public void setObtained_type(String obtained_type) {
        this.obtained_type = obtained_type;
    }
}

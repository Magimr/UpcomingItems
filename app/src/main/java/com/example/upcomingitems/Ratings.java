package com.example.upcomingitems;

public class Ratings {

    public float avgStars;
    public long totalPoints;
    public long numberVotes;

    public float getAvgStars() {
        return avgStars;
    }

    public void setAvgStars(float avgStars) {
        this.avgStars = avgStars;
    }

    public long getTotalPoints() {
        return totalPoints;
    }

    public void setTotalPoints(long totalPoints) {
        this.totalPoints = totalPoints;
    }

    public long getNumberVotes() {
        return numberVotes;
    }

    public void setNumberVotes(long numberVotes) {
        this.numberVotes = numberVotes;
    }
}
